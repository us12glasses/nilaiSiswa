    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        if (cbNisn.getSelectedIndex() == 0 && cbAbsensi.getSelectedIndex() == 0 && jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Harap Masukkan Data Terlebih Dahulu");
        } else if (cbNisn.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Harap Pilih NISN Terlebih Dahulu");
        } else if (cbAbsensi.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Harap Pilih Status Absensi Terlebih Dahulu");
        } else if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Harap Masukkan Tanggal Terlebih Dahulu");  
        } else {
            try{
                koneksi();
                String pattern = "yyyy-MM-dd";
                SimpleDateFormat sdf = new SimpleDateFormat(pattern);
                String date = String.valueOf(sdf.format(jDateChooser1.getDate()));
                String sql = "INSERT INTO absensi VALUES('"+teksKDAbsen.getText()+"','"+cbNisn.getSelectedItem()+"','"+cbAbsensi.getSelectedItem()+"','"+date+"')";
                stm.executeUpdate(sql);
                con.close();
                tampildata();
                bersih();
                autoID();
                JOptionPane.showMessageDialog(null, "Data berhasil di simpan");
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, "Proses penyimpanan gagal/koneksi gagal..");
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(null,"Kode Absen: "+teksKDAbsen.getText()+" sudah ada...!!!");
                System.out.println(e.getMessage());
                bersih();
            }
        }
    }//GEN-LAST:event_btnInsertActionPerformed

