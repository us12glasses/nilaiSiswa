                koneksi();
                String sql = "INSERT INTO kelas VALUES('"+teksKDKelas.getText()+"','"+teksNamakelas.getText()+"','"+cbNisn.getSelectedItem()+"','"+cbIDGuru.getSelectedItem()+"')";
                stm.executeUpdate(sql);
                con.close();
                tampildata();
                bersih();
                autoID();
                JOptionPane.showMessageDialog(null, "Data berhasil di simpan");
